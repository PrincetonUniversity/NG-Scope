typedef struct {
  int      n;
  uint32_t s;
  int      iterations;
  int      len;
  float    ebno;
  int      errors;
} expected_errors_t;

static expected_errors_t expected_errors[] = {{100, 1, 1, 504, 1.0, 3989},
                                              {100, 1, 2, 504, 1.0, 1922},
                                              {100, 1, 3, 504, 1.0, 1096},
                                              {100, 1, 4, 504, 1.0, 957},

                                              {100, 1, 1, 504, 2.0, 803},
                                              {100, 1, 2, 504, 2.0, 47},
                                              {100, 1, 3, 504, 2.0, 7},
                                              {100, 1, 4, 504, 2.0, 0},

                                              {100, 1, 1, 6144, 1.5, 24719},
                                              {100, 1, 2, 6144, 1.5, 897},
                                              {100, 1, 3, 6144, 1.5, 2},
                                              {100, 1, 4, 6144, 1.5, 0},
                                              {-1, 0, -1, -1, -1.0, -1}};

int get_expected_errors(int n, uint32_t s, int iterations, int len, float ebno)
{
  int i;
  i = 0;
  while (expected_errors[i].n != -1) {
    if (expected_errors[i].n == n && expected_errors[i].s == s && expected_errors[i].len == len &&
        expected_errors[i].iterations == iterations && expected_errors[i].ebno == ebno) {
      break;
    } else {
      i++;
    }
  }
  return expected_errors[i].errors;
}

